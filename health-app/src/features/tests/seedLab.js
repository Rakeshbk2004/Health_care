// ─── Helper utilities ─────────────────────────────────────────────────────────

export const KARNATAKA_DISTRICTS = [
  "All",
  ...Array.from(new Set(karnatakaLabs.map((l) => l.district))).sort(),
];

/** Filter labs by district */
export const getLabsByDistrict = (district) =>
  district === "All"
    ? karnatakaLabs
    : karnatakaLabs.filter((l) => l.district === district);

/** Search labs by query string across name, city, district, address, tests */
export const searchLabs = (query = "", district = "All") => {
  const q = query.toLowerCase().trim();
  return getLabsByDistrict(district).filter(
    (lab) =>
      !q ||
      lab.name.toLowerCase().includes(q) ||
      lab.city.toLowerCase().includes(q) ||
      lab.district.toLowerCase().includes(q) ||
      lab.address.toLowerCase().includes(q) ||
      lab.tests.some((t) => t.toLowerCase().includes(q)),
  );
};

export default karnatakaLabs;
